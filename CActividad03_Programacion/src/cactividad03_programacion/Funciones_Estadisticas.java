    private void ButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCalcularActionPerformed
    String entrada = txtNnumero.getText();
        
        // Convertir la entrada en un array de doubles
    double[] datos = convertirCadenaADoubles(entrada);

    // Calcular las estadísticas
    double media = calcularMedia(datos);
    double moda = calcularModa(datos);
    double mediana = calcularMediana(datos);
    double desviacionEstandar = calcularDesviacionEstandar(datos);
    double varianza = calcularVarianza(datos);
    
    // Mostrar los resultados en el JTextArea
    String resultados = 
                        "====RESULTADOS===="+ "\n" +
                        "Media: " + media + "\n" +
                        "Moda: " + moda + "\n" +
                        "Mediana: " + mediana + "\n" +
                        "Desviación Estándar: " + desviacionEstandar + "\n" +
                        "Varianza: " + varianza;
    textAreaResultado.setText(resultados);
    }//GEN-LAST:event_ButtonCalcularActionPerformed

