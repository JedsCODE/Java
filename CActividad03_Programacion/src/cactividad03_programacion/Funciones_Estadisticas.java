    private double calcularMedia(double[] datos) {
        double suma = 0;
        for (double dato : datos) {
            suma += dato;
        }
        return suma / datos.length;
    }
    
    private double calcularModa(double[] datos) {
        Arrays.sort(datos);
        int maxContador = 0;
        int contador = 1;
        double moda = datos[0];
        double actual = datos[0];

        for (int i = 1; i < datos.length; i++) {
            if (datos[i] == actual) {
                contador++;
            } else {
                if (contador > maxContador) {
                    maxContador = contador;
                    moda = actual;
                }
                contador = 1;
                actual = datos[i];
            }
        }

        if (contador > maxContador) {
            moda = actual;
        }

        return moda;
    }
    
    private double calcularMediana(double[] datos) {
        Arrays.sort(datos);
        if (datos.length % 2 == 0) {
            return (datos[datos.length / 2 - 1] + datos[datos.length / 2]) / 2;
        } else {
            return datos[datos.length / 2];
        }
    }
    
